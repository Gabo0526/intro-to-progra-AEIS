#include <stdio.h>

int main()
{
    // Operador de Asignacion:
    int entero = 0; // Inicializacion de variable

    entero = 10;

    // Operador de suma y asignacion:
    entero += 5; // entero = entero + 5

    // Operador de resta y asignacion:
    entero -= 10; // entero = entero - 10

    printf("%d\n", entero);

    return 0;
}